using UnityEngine;

public class GridPosition
{
    public GridPosition(int column, int row)
    {
        Column = column;
        Row = row;
    }

    public int Column;
    public int Row;
}

public class VirtualPosition
{
    public VirtualPosition(int column, int row)
    {
        Column = column;
        Row = row;
    }

    public int Column;
    public int Row;
}
